     110: `**TOPIC: REGULAR VERBS (క్రమబద్ధమైన క్రియలు)**
**Introduction:**
ఇంగ్లీష్‌లో చాలా వరకు క్రియలు (Verbs) ఒక పద్ధతిని పాటిస్తాయి. గతం (Past) గురించి చెప్పడానికి వీటి చివర కేవలం **'-ed'** లేదా **'-d'** చేరిస్తే సరిపోతుంది. వీటిని 'Regular Verbs' అంటారు.

**The Golden Rule:**
* **V2 (Past):** Add '-ed'
* **V3 (Past Participle):** Add '-ed'

**Spelling Rules (ముఖ్యమైన స్పెల్లింగ్ సూత్రాలు):**

1. **General Rule (సాధారణంగా):**
   చాలా పదాలకు చివర 'ed' చేర్చాలి.
   * Play + ed = **Played** (ఆడెను)
   * Work + ed = **Worked** (పని చేసెను)
   * Watch + ed = **Watched** (చూసెను)

2. **Ending with 'e' ('e' తో అంతమయ్యేవి):**
   చివర ఆల్రెడీ 'e' ఉంటే, కేవలం 'd' చేర్చాలి.
   * Like + d = **Liked** (ఇష్టపడెను)
   * Love + d = **Loved** (ప్రేమించెను)
   * Dance + d = **Danced** (నాట్యం చేసెను)

3. **Ending with 'y' ('y' తో అంతమయ్యేవి):**
   'y' కి ముందు హల్లు (Consonant) ఉంటే, 'y' తీసేసి **'ied'** పెట్టాలి.
   * Cry -> Cr + ied = **Cried** (ఏడ్చెను)
   * Study -> Stud + ied = **Studied** (చదివెను)
   * Try -> Tr + ied = **Tried** (ప్రయత్నించెను)
   *(Note: 'y' ముందు అచ్చు (a,e,i,o,u) ఉంటే కేవలం 'ed' వస్తుంది. Ex: Play -> Played).*

4. **Double Consonant (అక్షరం రెట్టింపు అవ్వడం):**
   చిన్న పదాలకు చివర ఉన్న అక్షరం రెండు సార్లు వస్తుంది.
   * Stop -> Stop + p + ed = **Stopped** (ఆగెను)
   * Plan -> Plan + n + ed = **Planned** (ప్లాన్ చేసెను)

**Examples List (V1 - V2 - V3):**
* Ask - Asked - Asked (అడుగు)
* Call - Called - Called (పిలుచు)
* Help - Helped - Helped (సహాయం చేయు)
* Look - Looked - Looked (చూడు)
* Move - Moved - Moved (కదులు)
* Start - Started - Started (మొదలుపెట్టు)
* Talk - Talked - Talked (మాట్లాడు)
* Walk - Walked - Walked (నడుచు)`,

